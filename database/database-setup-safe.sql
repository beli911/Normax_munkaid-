    WHERE tablename = 'timesheets' 
    AND policyname = 'Admin törölheti'
  ) THEN
    CREATE POLICY "Admin törölheti" ON timesheets 
    FOR DELETE USING (exists (select 1 from profiles where id = auth.uid() and is_admin = true));
  END IF;
END $$;

DO $$
